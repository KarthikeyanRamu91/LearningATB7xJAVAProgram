package ex_14072024;

public class Lab060 {
    public static void main(String[] args) {
//        increment ++ decrement -- operators
        int a=10;
        int b= ++a; // a = a+1;
        System.out.println(a);
        System.out.println(b);
        // Exp = 11 , a = 11

        int c=20;
        int d= c++;
        System.out.println(c);
        System.out.println(d);
        // Exp = 10 , a = 11

    }
}
