package ex_13072024;

public class Lab015 {
    public static void main(String[] args) {

//        data_type variable_name = variable_value (literal)
//        data_type
//        primitive data_type
//        boolean
//        true or false

        boolean is_married=true;
        boolean are_you_animal=false;
        System.out.println(is_married);
        System.out.println(are_you_animal);

//        numeric byte, short, int, long and char

        byte b=22;
        short s= 100;
        int i= 1234;
        long l= 9791133234l;
        char grade='a';

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(grade);

//        floating
        float pi=3.14f;
//        double
        double d=123.456;
        System.out.println(pi);
        System.out.println(d);


//        non-primitive data_type String
        String name="Karthik";
        System.out.println(name);



    }
}
